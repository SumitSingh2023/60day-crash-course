let container = document.getElementById("product-cont")
let inputBox = document.getElementById("search-input")
let btn = document.getElementById("search")

function showData(arr) {
    container.innerHTML=""
    arr.forEach(function (ele) {
        let card = document.createElement("div")
        card.className = "card"
        let img = document.createElement("img")
        img.src = ele.img
        img.className = "image"

        let title = document.createElement("h3")
        title.innerHTML = ele.title

        let desc = document.createElement("p")
        desc.innerHTML = ele.desc

        let price = document.createElement("h5")
        price.innerHTML = ele.price

        card.append(img, title, desc, price)
        container.append(card)

    })

}

function searchData() {
    let value = inputBox.value
    let narr = product.filter((ele) => {
        return ele.title.toLowerCase().includes(value.toLowerCase())
    })
    showData(narr)
    
}

btn.addEventListener("click", searchData)
showData(product)


